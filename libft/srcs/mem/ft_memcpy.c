#include "../../inc/libft.h"

void	*ft_memcpy(void *dst, const void *src, size_t n)
{
	size_t	i;

	i = 0;
	if (!dst || !src)
		return (NULL);
	if (dst == src)
		return (dst);
	while (n-- > 0)
		((unsigned char *)dst)[i++] = *(unsigned char *)src++;
	return (dst);
}
